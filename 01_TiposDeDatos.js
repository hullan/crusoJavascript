/**
 * Tipos de datos
 */

//numero - number
console.log(27);
console.log(27.56);

//Texto - String
console.log("Ignacio Garcia");
console.log("Pepito Perez");
console.log(`Hola, Soy Ignacio`);

//Logicos - Booleanos
console.log(true);
console.log(false);

//indefinido - undefined
console.log(undefined);

//null
console.log(null);

/**
 * Tipos de datos estructurales
 */

//Objeto - object
console.log({
  nombre: "Ignacio",
  edad: 37,
  fechaDeNacimiento: null,
  esAlto: false,
  ciudades: ["Zaragoza", "Madrid"],
  direccion: { callePrincial: "Salomón y la reina de saba" },
});

//tipo de dato de tipo lista - Array
console.log(["España", "Mexico", "Ecuador"]);

console.log([21, true, { nombre: "Ignacio" }, [1, 2, 3, 4, 5]]);
